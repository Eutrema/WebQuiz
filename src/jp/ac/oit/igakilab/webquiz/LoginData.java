package jp.ac.oit.igakilab.webquiz;

public class LoginData {
	private String ID;
	private String password;

	public LoginData() {
	}

	public String getID() {
		return ID;
	}
	public void setID(String ID) {
		this.ID = ID;
	}
	public String getpassword() {
		return password;
	}
	public void setpassword(String password) {
		this.password = password;
	}
}